/*
used to extract 
a portion of an array or a string without modifying the original one.

slice(start, end)

*/

// Array 
let numbers = [1,3,5,6,7]
let newNumbers=numbers.slice(1,7)
console.log(newNumbers);

// String Name

let student = "Ravi Shankar Singh "
let newStudent = student.slice(0,9)
console.log(newStudent);

