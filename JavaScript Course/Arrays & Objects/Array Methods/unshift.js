/*

used to add one or more elements 
to the beginning of an array and returns the new length of the array.

Syntax:
array.unshift(element1, element2, ..., elementN);

*/

let num = [1,3,4]
num.unshift(2);
console.log(num);

let Name = ["Ravi", "Sukanyaa", "Divyanshu"];
Name.unshift("Shankar","Singh", "Sukanyaa Singh")
console.log(Name);
